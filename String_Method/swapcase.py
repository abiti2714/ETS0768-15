# Demonstrating the use of the swapcase() method in Python
# The swapcase() method returns a new string where all uppercase letters are converted to lowercase,
# and all lowercase letters are converted to uppercase.

# Example 1: Swapping case in a mixed-case string
string = "Python IS Awesome!"
result = string.swapcase()
print(result)
# Expected output: "pYTHON is aWESOME!"

# Example 2: Swapping case in a string with numbers and special characters
string_2 = "123 Python!"
result_2 = string_2.swapcase()
print(result_2)
# Expected output: "123 pYTHON!"
