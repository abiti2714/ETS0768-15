# Example 1: Creating Proper Names from User Input
# Convert a user's name to title case (capitalize the first letter of each word)
user_name = "KiBRoM NaSiR"
formatted_name = user_name.title()
print(formatted_name)  # Expected output: Kibrom Nasir

